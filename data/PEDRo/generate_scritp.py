def write_split(file_name, records):
    """
    Generate a split file with sequential frame names based on the index ranges.
    Args:
        file_name (str): Output file name.
        records (list): List of tuples containing (rec_name, start_idx, end_idx).
    """
    with open(file_name, "w") as f:
        for _, start, end in records:  # Ignore 'rec_name' and only use index ranges
            for idx in range(start, end + 1):
                f.write(f"frame{idx:07d}\n")  # Write sequential frame names
    print(f"Generated: {file_name}")

